/**
 * Text-safe area (вписанный прямоугольник) внутри фигуры.
 *
 * Текст фигуры не должен выходить за её видимый контур. Bounding box фигуры
 * прямоугольный, но сама фигура (треугольник, ромб, круг, стрелка, параллелограмм)
 * занимает лишь часть бокса. Здесь считаем максимальный прямоугольник, который
 * гарантированно лежит внутри контура — по нему позиционируется и статический
 * HTML-оверлей текста, и inline-редактор.
 *
 * Геометрия форм — из ShapeDrawer._drawFillShape (тот же контур).
 *
 * @param {string} kind — square|rounded|circle|triangle|diamond|parallelogram|arrow
 * @param {number} w — ширина bounds (world px)
 * @param {number} h — высота bounds (world px)
 * @returns {{left:number, top:number, width:number, height:number}} область в локальных координатах бокса (0,0 — левый верх)
 */
// Доля запаса внутрь контура для непрямоугольных фигур. Вписанный прямоугольник и
// клип-контур касаются краёв фигуры углами/сторонами, поэтому глифы верхней строки
// при переполнении «липнут» к наклонной стороне (для круга — углы вписанного квадрата
// лежат ровно на окружности). Ужимаем безопасную зону на этот множитель, чтобы текст
// не доходил до самого контура. 0.8 даёт видимый зазор (~20% радиуса) и служит буфером
// против небольшого смещения бокса относительно центра фигуры.
const SHAPE_INNER_MARGIN = 0.8;

export function getShapeTextSafeArea(kind, w, h) {
    const W = Math.max(0, w || 0);
    const H = Math.max(0, h || 0);

    switch (kind) {
        case 'circle': {
            // Вписанный прямоугольник в эллипс (полуоси W/2, H/2): множитель 1/√2.
            // Углы такого прямоугольника лежат точно на окружности, поэтому дополнительно
            // ужимаем на SHAPE_INNER_MARGIN — иначе крайние строки касаются контура.
            const f = (1 / Math.SQRT2) * SHAPE_INNER_MARGIN;
            const iw = W * f;
            const ih = H * f;
            return { left: (W - iw) / 2, top: (H - ih) / 2, width: iw, height: ih };
        }
        case 'triangle': {
            // Вершина сверху (W/2,0), основание снизу. Ширину берём по вписанному в
            // основание прямоугольнику (W/2), но по вертикали центрируем на середине
            // фигуры, а не прижимаем к низу — текст должен стоять по центру и расти
            // вверх при переполнении. Верхние углы box'а выходят за наклонные стороны,
            // но их прячет клип-контур (getShapeTextClipPath) с внутренним отступом.
            return { left: W * 0.25, top: H * 0.25, width: W * 0.5, height: H * 0.5 };
        }
        case 'diamond': {
            // Ромб с вершинами по серединам сторон: вписанный центрированный
            // прямоугольник — половина ширины и высоты. Его углы лежат на сторонах
            // ромба, поэтому ужимаем на SHAPE_INNER_MARGIN, чтобы текст не касался края.
            const dw = W * 0.5 * SHAPE_INNER_MARGIN;
            const dh = H * 0.5 * SHAPE_INNER_MARGIN;
            return { left: (W - dw) / 2, top: (H - dh) / 2, width: dw, height: dh };
        }
        case 'parallelogram': {
            // skew совпадает с ShapeDrawer. Безопасна средняя вертикальная полоса.
            const skew = Math.min(W * 0.25, 20);
            return { left: skew, top: 0, width: Math.max(0, W - 2 * skew), height: H };
        }
        case 'arrow': {
            // Прямоугольное «древко» стрелки — единственная зона с полной высотой полосы.
            const shaftH = Math.max(6, H * 0.3);
            const shaftY = (H - shaftH) / 2;
            return { left: 0, top: shaftY, width: W * 0.6, height: shaftH };
        }
        case 'square':
        case 'rounded':
        default:
            return { left: 0, top: 0, width: W, height: H };
    }
}

/**
 * CSS clip-path для текстового слоя фигуры — чтобы текст, вышедший за наклонные
 * стороны, обрезался контуром («прятался внутри фигуры»), а не торчал наружу.
 *
 * Координаты заданы в системе САМОГО слоя (он позиционируется по safe-area из
 * getShapeTextSafeArea). Для большинства фигур safe-area целиком лежит внутри
 * контура, поэтому клип не нужен (null). Исключение — треугольник: верх safe-area
 * (центральный прямоугольник W/2×H/2) шире контура, поэтому клипуем трапецией.
 * Трапеция задана с внутренним отступом от наклонных сторон (не 25%/0, а 29%/5%),
 * чтобы верхняя строка при переполнении не касалась контура, а обрезалась чуть внутри.
 *
 * @param {string} kind
 * @returns {string|null}
 */
export function getShapeTextClipPath(kind) {
    if (kind === 'triangle') {
        return 'polygon(29% 5%, 71% 5%, 93% 95%, 7% 95%)';
    }
    return null;
}
